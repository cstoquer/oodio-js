/** Main abstract class for modules. Handle UI display.
 *
 * @author Cedric Stoquer
 */

function Module(params) {
	var dom = createDom('module x' + this.description_moduleSize);
	dom.textContent = this.description_moduleName;
	this._dom = dom;
}

