// utils
function map(value, iMin, iMax, oMin, oMax) {
	return oMin + (oMax - oMin) * (value - iMin) / (iMax - iMin);
}

function noteToFreq(midiNoteNumber) {
	return 440 * Math.pow(2, (midiNoteNumber - 69) / 12);
}